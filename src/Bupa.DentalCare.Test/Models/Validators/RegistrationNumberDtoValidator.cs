using FluentValidation;

namespace Bupa.DentalCare.Test.Models.Validators;

public class RegistrationNumberDtoValidator : AbstractValidator<RegistrationNumberDto>
{
    public RegistrationNumberDtoValidator()
    {
        RuleFor(registrationNumber => registrationNumber.RegistrationNumber)
            .NotEmpty().WithMessage("Registration Number is required")
            .Matches(
                "(^[A-Z]{2}[0-9]{2}\\s?[A-Z]{3}$)|(^[A-Z][0-9]{1,3}[A-Z]{3}$)|(^[A-Z]{3}[0-9]{1,3}[A-Z]$)|(^[0-9]{1,4}[A-Z]{1,2}$)|(^[0-9]{1,3}[A-Z]{1,3}$)|(^[A-Z]{1,2}[0-9]{1,4}$)|(^[A-Z]{1,3}[0-9]{1,3}$)|(^[A-Z]{1,3}[0-9]{1,4}$)|(^[0-9]{3}[DX]{1}[0-9]{3}$)")
            .WithMessage("Invalid Registration Number, please check again");
    }
    
    public Func<object, string, Task<IEnumerable<string>>> ValidateValue => async (model, propertyName) =>
    {
        var result = await ValidateAsync(ValidationContext<RegistrationNumberDto>.CreateWithOptions((RegistrationNumberDto)model, x => x.IncludeProperties(propertyName)));
        if (result.IsValid)
            return Array.Empty<string>();
        return result.Errors.Select(e => e.ErrorMessage);
    };
}